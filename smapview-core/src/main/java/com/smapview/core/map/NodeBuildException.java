package com.smapview.core.map;

public class NodeBuildException extends Exception {

	private static final long serialVersionUID = -6175965142938924886L;

	public NodeBuildException(String message) {
		super(message);
	}

	public NodeBuildException(Throwable cause) {
		super(cause);
	}

	public NodeBuildException(String message, Throwable cause) {
		super(message, cause);
	}

}
